	<div id="historique">Historique de la salsa :</div><br/>
	La salsa est née dans les années 60 à New York aux États Unis dans les quartiers hispaniques (Spanish Harlem) inventée par les barrios des quartiers pauvres latinos. Cette musique est leur seul lien d'attache avec leur pays d'origine, avec leurs racines et bien souvent ils ne comprennent pas la valeur et le poids des mots, mais ils savent la danser, et connaissent les rythmes.<br/>
	La Salsa incarne le métissage culturel et la revendication contre l'oppression Américaine tout cela dans un esprit de convivialité.<br/>
	Dans les années 70 la salsa s'épanouie dans les quartiers cubains de New-York.<br/>
	<br/>
	La salsa cubaine est une danse très dynamique sur la piste. C'est ce style de salsa qui permet de danser la "Rueda" (une danse où les couples forment un cercle, font les passes de manière synchronisée et où les danseuses passent d'un danseur à l'autre). Dans ce style, les danseurs "marchent" beaucoup. Ainsi, les danseuses ne font pas de tours comme en rock par exemple: les tours sont marchés.<br/>
	Pas de base : en cubaine, le pas de base est arrière/arrière. Il se fait presque "sur place", en se déhanchant.</p><br/><br/>
